package com.bhubanananda.logic;

/**
 * The Class Pattern2.
 *
 * @author Bhubanananda
 */
public class Pattern2 {

	/**
	 * The main method.
	 *
	 * @param args the arguments
	 */
	public static void main(String[] args) {

		for (int i = 1; i <= 5; i++) {
			for (int j = 1; j <= 5; j++) {
				System.out.print(j);
			}
			System.out.println();
		}
	}

}

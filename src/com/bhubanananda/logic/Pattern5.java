package com.bhubanananda.logic;

/**
 * The Class Pattern5.
 *
 * @author Bhubanananda
 */
public class Pattern5 {

	/**
	 * The main method.
	 *
	 * @param args the arguments
	 */
	public static void main(String[] args) {

		for (int i = 5; i >= 1; i--) {
			for (int j = 5; j >= 1; j--) {
				System.out.print(j);
			}
			System.out.println();
		}

	}

}

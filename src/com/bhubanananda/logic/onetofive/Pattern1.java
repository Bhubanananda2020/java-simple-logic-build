package com.bhubanananda.logic.onetofive;

/**
 * The Class Pattern1.
 *
 * @author Bhubanananda
 */
public class Pattern1 {

	/**
	 * The main method.
	 *
	 * @param args the arguments
	 */
	public static void main(String[] args) {

		for (int i = 0; i < 5; i++) {
			for (int j = 0; j < 5; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
	}
}
